class Color:
    """Códigos de cores ANSI para saída no terminal."""
    RESET = "\033[0m"      # Resetar para cor padrão
    BLACK = "\033[30m"     # Preto
    RED = "\033[31m"       # Vermelho
    GREEN = "\033[32m"     # Verde
    YELLOW = "\033[33m"    # Amarelo
    BLUE = "\033[34m"      # Azul
    MAGENTA = "\033[35m"   # Magenta
    CYAN = "\033[36m"      # Ciano
    WHITE = "\033[37m"     # Branco
    BOLD = "\033[1m"       # Negrito
